#!/bin/bash
# koi-executor.sh — Módulo de ejecución de trabajo para Operación Sustento
# Funciones compartidas por todos los workers para ejecutar trabajos ganados.
#
# Uso: source koi-executor.sh

# Detectar el tipo de trabajo y generar el deliverable
execute_job() {
    local PLATFORM="$1"
    local JOB_ID="$2"
    local JOB_TITLE="$3"
    local JOB_DESCRIPTION="$4"
    local JOB_DELIVERABLES="$5"
    local JOB_CATEGORY="$6"

    local EXEC_LOG="$HOME/.openwork/executor.log"
    log_exec() {
        echo "[$(date '+%Y-%m-%d %H:%M:%S')] [$PLATFORM] $1" >> "$EXEC_LOG"
    }

    log_exec "=== Ejecutando trabajo: $JOB_TITLE (id: $JOB_ID) ==="
    log_exec "Categoría: $JOB_CATEGORY"
    log_exec "Entregables: $JOB_DELIVERABLES"

    # Generar el deliverable según la categoría
    local DELIVERABLE=""
    case "$JOB_CATEGORY" in
        research)
            DELIVERABLE=$(execute_research "$JOB_TITLE" "$JOB_DESCRIPTION" "$JOB_DELIVERABLES")
            ;;
        content|writing)
            DELIVERABLE=$(execute_content "$JOB_TITLE" "$JOB_DESCRIPTION" "$JOB_DELIVERABLES")
            ;;
        data|analysis)
            DELIVERABLE=$(execute_data "$JOB_TITLE" "$JOB_DESCRIPTION" "$JOB_DELIVERABLES")
            ;;
        code|development)
            DELIVERABLE=$(execute_code "$JOB_TITLE" "$JOB_DESCRIPTION" "$JOB_DELIVERABLES")
            ;;
        *)
            DELIVERABLE=$(execute_generic "$JOB_TITLE" "$JOB_DESCRIPTION" "$JOB_DELIVERABLES")
            ;;
    esac

    echo "$DELIVERABLE"
}

